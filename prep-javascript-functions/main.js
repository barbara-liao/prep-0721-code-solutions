function addTwoNumbers(x, y) {
  return x + y;
}

var sum = addTwoNumbers(4, 4);
console.log('sum', sum);

function convertHoursToMinutes(x) {
  return x * 60;
}

var multiply = convertHoursToMinutes(3);
console.log('multiply', multiply);

function getGreeting(name) {
  return 'Hello' + ' ' + name + '!';
}

var greeting = getGreeting('Sunshine');
console.log('greeting', greeting);

function addAndMultiplyBy5(a, b) {
  return (a + b) * 5;
}

var addMultiply = addAndMultiplyBy5(3, 5);
console.log('addMultiply', addMultiply);

function subtractTwoNumbers(c, d) {
  return c - d;
}

var subtract = subtractTwoNumbers(20, 5);
console.log('subtract', subtract);

function getCircleCircumference(r) {
  return 2 * (22 / 7) * r;
}

var circumference = getCircleCircumference(4);
console.log('circumference', circumference);

function getFullName(f, l) {
  return f + ' ' + l;
}

var fullName = getFullName('Barbara', 'Liao');
console.log('fullName', fullName);

function cube(c) {
  return c ** 3;
}

var area = cube(4);
console.log('area', area);
